fn solve() {
    let s = std::io::stdin();
    let mut sc = Scanner { stdin: s.lock() };
    let n: usize = sc.read();
    let m: usize = sc.read();
    let mut a: Vec<usize> = sc.vec(n);
    a.sort();
    let mut bc: Vec<(usize, usize)> = (0..m)
        .map(|_| {
            let b = sc.read();
            let c = sc.read();
            (b, c)
        })
        .collect();
    bc.sort_by_key(|k| k.1);
    bc.reverse();
    let mut idx = 0;
    for &(b, c) in bc.iter() {
        let mut tmp_b = b;
        while tmp_b > 0 && idx < n && a[idx] < c {
            a[idx] = c;
            idx += 1;
            tmp_b -= 1;
        }
    }
    let sum = a.iter().fold(0, |sum, a| sum + a);
    println!("{}", sum);
}

fn main() {
    std::thread::Builder::new()
        .stack_size(64 * 1024 * 1024) // 64MB
        .spawn(|| solve())
        .unwrap()
        .join()
        .unwrap();
}

