fn solve() {
    let s = std::io::stdin();
    let mut sc = Scanner { stdin: s.lock() };
    let order_times: Vec<u32> = (0..5).map(|_| sc.read::<u32>()).collect();
    let mut res: Vec<u32> = order_times.iter().map(|&x| (10 - (x % 10)) % 10).collect();
    res.sort();
    let wait = res[0..4].iter().fold(0, |sum, a| sum + a);
    let ans = order_times.iter().fold(0, |sum, a| sum + a) + wait;
    println!("{}", ans);
}

fn main() {
    std::thread::Builder::new()
        .stack_size(64 * 1024 * 1024) // 64MB
        .spawn(|| solve())
        .unwrap()
        .join()
        .unwrap();
}

