fn solve() {
    let s = std::io::stdin();
    let mut sc = Scanner { stdin: s.lock() };
    let n: usize = sc.read();
    let a: Vec<i64> = sc.vec(n);
    let mut cnt = 0;
    let mut b: Vec<i64> = a.iter().map(|&x| {
        if x <= 0 {
            cnt += 1;
            return x * -1;
        }
        x
    }).collect();
    b.sort();
    let mut res:i64 = b.iter().sum();
    if cnt % 2 == 0 {
        println!("{}", res);
        return;
    }

    res -= 2 * b[0];

    println!("{}", res);
}

fn main() {
    std::thread::Builder::new()
        .stack_size(64 * 1024 * 1024) // 64MB
        .spawn(|| solve())
        .unwrap()
        .join()
        .unwrap();
}

