fn solve() {
    let s = std::io::stdin();
    let mut sc = Scanner { stdin: s.lock() };
    let a: f64 = sc.read();
    let b: f64 = sc.read();
    let x: f64 = sc.read();
    if a * a * b / 2.0 >= x {
        let h = 2.0 * x / (a * b);
        println!("{}", 90.0 - h.atan2(b).to_degrees());
        return;
    } else {
        let c = 2.0 * (a * a * b - x) / (a * a);
        println!("{}", 90.0 - (a / c).atan().to_degrees());
    }
}

fn main() {
    std::thread::Builder::new()
        .stack_size(64 * 1024 * 1024) // 64MB
        .spawn(|| solve())
        .unwrap()
        .join()
        .unwrap();
}

