fn solve() {
    let s = std::io::stdin();
    let mut sc = Scanner { stdin: s.lock() };
    let n: usize = sc.read();
    let k: usize = sc.read();
    let mut hs: Vec<usize> = sc.vec(n);
    hs.sort();
    let mut result = 10e9 as u64;
    for i in 0..n {
        if i + k - 1 >= n {
            break;
        }
        let tmp = (hs[i + k - 1] - hs[i]) as u64;
        result = std::cmp::min(result, tmp);
    }
    println!("{}", result);
}

fn main() {
    std::thread::Builder::new()
        .stack_size(64 * 1024 * 1024) // 64MB
        .spawn(|| solve())
        .unwrap()
        .join()
        .unwrap();
}

